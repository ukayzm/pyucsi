class BitStream:
    def __init__(self, buf):
        self.buf = buf
        self.seek(0)

    def seek(self, pos):
        self.pos = pos

    def remain(self):
        return len(self.buf) * 8 - self.pos

    def align_byte(self):
        self.pos = int((self.pos + 7) / 8) * 8

    def read(self, num_bits):
        if (int(self.pos / 8) >= len(self.buf)):
            return -1
        if (self.remain() < num_bits):
            return -1
        val = 0
        for i in range(0, num_bits):
            index = int(self.pos / 8)
            shift = 7 - (self.pos % 8)
            val <<= 1
            val |= (self.buf[index] >> shift) & 1
            self.pos += 1
        return val


